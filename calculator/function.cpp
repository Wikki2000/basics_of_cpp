
/**
 * sum - Add two numbers
 * @a: First number
 * @b: Second number
 *
 * Return: The sum of two numbers
 */
float sum(float a, float b)
{
	return (a + b);
}

/**
 * sub - Calculate the difference of two numbers
 * @a: First number
 * @b: Second number
 *
 * Return: The difference of two numbers
 */
float sub(float a, float b)
{
	return (a - b);
}

/**
 * mul - Calculate the product of two numbers
 * @a: First number
 * @b: Second number
 *
 * Return: The product of two numbers
 */
float mul(float a, float b)
{
	return (a * b);
}

/**
 * div - Division of two numbers
 * @a: First number
 * @b: Second number
 *
 * Return: The division of two bumbera
 */
float div(float a, float b)
{
	return (a / b);
}

